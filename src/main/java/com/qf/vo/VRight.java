package com.qf.vo;

public class VRight {
	private Integer rightCode;
	private String  rightName;
	private String rightPname;
	private Integer rightFlag;
	public Integer getRightCode() {
		return rightCode;
	}
	public void setRightCode(Integer rightCode) {
		this.rightCode = rightCode;
	}
	public String getRightName() {
		return rightName;
	}
	public void setRightName(String rightName) {
		this.rightName = rightName;
	}
	public String getRightPname() {
		return rightPname;
	}
	public void setRightPname(String rightPname) {
		this.rightPname = rightPname;
	}
	public Integer getRightFlag() {
		return rightFlag;
	}
	public void setRightFlag(Integer rightFlag) {
		this.rightFlag = rightFlag;
	}
	

}
